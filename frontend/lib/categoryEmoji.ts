export function getCategoryEmoji(
  title: string | null | undefined,
  emoji?: string | null,
): string | null {
  if (emoji) return emoji;
  if (!title) return null;
  for (const [re, e] of RULES) {
    if (re.test(title)) return e;
  }
  return null;
}

