def main():
    with Session() as db:
        # Upsert categories
        for cat in CATEGORIES:
            db.execute(text("""
                INSERT INTO flashcard_categories (id, name, emoji, description, sort_order)
                VALUES (:id, :name, :emoji, :description, :sort_order)
                ON CONFLICT (id) DO UPDATE SET
                    name = EXCLUDED.name,
                    emoji = EXCLUDED.emoji,
                    description = EXCLUDED.description,
                    sort_order = EXCLUDED.sort_order
            """), cat)

        # Reset sequence
        db.execute(text("SELECT setval('flashcard_categories_id_seq', (SELECT MAX(id) FROM flashcard_categories))"))

        # Insert cards
        existing = db.execute(text("SELECT word FROM flashcards")).fetchall()
        existing_words = {r[0] for r in existing}

        inserted = 0
        for i, card in enumerate(CARDS):
            cat_id, word, short_def, simple_exp, example, difficulty = card
            if word in existing_words:
                continue
            db.execute(text("""
                INSERT INTO flashcards (category_id, word, short_definition, simple_explanation, example, difficulty, sort_order)
                VALUES (:cat_id, :word, :short_def, :simple_exp, :example, :difficulty, :sort_order)
            """), {
                "cat_id": cat_id, "word": word, "short_def": short_def,
                "simple_exp": simple_exp, "example": example,
                "difficulty": difficulty, "sort_order": i,
            })
            inserted += 1

        db.commit()
        total = db.execute(text("SELECT COUNT(*) FROM flashcards")).scalar()
        print(f"✅ Inserted {inserted} new cards. Total in DB: {total}")


if __name__ == "__main__":
    main()
